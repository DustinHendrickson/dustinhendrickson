<i>Commands</i></br>
Create a new character: <b>/msg Fight|Bot @fight create</b></br>
See enemy stats: <b>/msg Fight|Bot @fight info playername</b></br>
Need help? <b>/msg Fight|Bot @fight help</b><br>
To Duel <b>/msg Fight|Bot @fight duel PLAYERNAME EXPTOBET</b> this will initiate a fight where you bet your EXP you'll win, minimum of 10 exp per bet.<br>
Want to try a quest? <b>/msg Fight|Bot @fight quest</b> this costs 15 exp, chance to get bonus exp or new weapons of your level.
