<i>Commands</i></br>

<b>/msg Fight|Bot @fight create</b><br>
Creates a new character</br>

<b>/msg Fight|Bot @fight info &lt;playername&gt;</b></br>
Shows the enemy stats.<br>

<b>/msg Fight|Bot @fight help</b><br>
Displays the help file.<br>

<b>/msg Fight|Bot @fight duel &lt;playername&gt; &lt;exp to bet&gt;</b><br>
This will initiate a fight where you bet your EXP you'll win, minimum of 10 exp per bet.<br>

<b>/msg Fight|Bot @fight quest</b><br>
Go on a quest, this costs 15 exp, chance to get bonus exp or new weapons of your level.<br>

